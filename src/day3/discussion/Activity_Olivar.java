public class Activity_Olivar {
    public static void main(String[] args) {

           // Declare and initialize variables
           boolean isStudent = true;
           byte age = 35;
           short yearLevel = 2;
           int population = 1000000;
           long nationalId = 12345678901L;
           float gradeAverage = 89.75f;
           double salary = 50000.50;
           char section = 'A';
           String fullName = "Jeffrey Olivar";

           // Print the values
           System.out.println("Boolean value: " + isStudent);
           System.out.println("Byte value: " + age);
           System.out.println("Short value: " + yearLevel);
           System.out.println("Int value: " + population);
           System.out.println("Long value: " + nationalId);
           System.out.println("Float value: " + gradeAverage);
           System.out.println("Double value: " + salary);
           System.out.println("Char value: " + section);
           System.out.println("String value: " + fullName);
       }
        
}
